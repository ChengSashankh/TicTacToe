class IntegerTriple{

    int first;
    int second;
    int third;

    public IntegerTriple(int first, int second, int third){
        this.first = first;
        this.second = second;
        this.third = third;
    }

    int f(){
        return this.first;
    }

    int s(){
        return this.second;
    }

    int t(){
        return this.third;
    }

}